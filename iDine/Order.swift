import SwiftUI

// Anything that conforms to ObservableObject can be used inside SwiftUI,
// and publish announcements when its values have changed so the user interface can be updated.

// Using the @Published property wrapper is a way of publishing change announcements

class Order: ObservableObject {
    @Published var items = [MenuItem]()

    var total: Int {
        if items.count > 0 {
            return items.reduce(0) { $0 + $1.price }
        } else {
            return 0
        }
    }

    func add(item: MenuItem) {
        items.append(item)
    }

    func remove(item: MenuItem) {
        if let index = items.firstIndex(of: item) {
            items.remove(at: index)
        }
    }
}
